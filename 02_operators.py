# OPERATORS ARITMETICOS
 
print(2 + 3)  # Addition. Suma dos numeros y devuelve el resultado.
print(5 - 2)  # Subtraction. Resta el segundo numero del primero y devuelve el resultado.
print(4 * 3)  # Multiplication. Multiplica dos numeros y devuelve el resultado.
print(10 / 2) # Division. Divide el primer numero por el segundo y devuelve el resultado.
print(10 // 3) # Floor Division. Divide el primer numero por el segundo y devuelve la parte entera del resultado.
print(2 ** 3) # Exponentiation. Eleva el primer numero a la potencia del segundo numero.
print(10 % 3) # Modulus. Devuelve el resto de la division del primer numero por el segundo.
print(2 ** 3 + 5 - 4 / 6 // 9) # Order of Operations. Sigue el orden de las operaciones matematicas para calcular el resultado.

# Concatenacion de strings. Une dos cadenas de texto y devuelve el resultado.
print("hola" + " python")
# Repeticion de strings. Repite una cadena de texto un numero especificado de veces y devuelve el resultado. 
print("hola " * (2 ** 3)) 

# OPERATORS COMPARISON

print(5 > 3)   # Greater than. Devuelve True si el primer numero es mayor que el segundo, de lo contrario devuelve False.
print(5 < 3)   # Less than. Devuelve True si el primer numero es menor que el segundo, de lo contrario devuelve False.
print(5 == 5)  # Equal to. Devuelve True si ambos numeros son iguales, de lo contrario devuelve False.
print(5 != 3)  # Not equal to. Devuelve True si ambos numeros son diferentes, de lo contrario devuelve False.
print(5 >= 5)  # Greater than or equal to. Devuelve True si el primer numero es mayor o igual que el segundo, de lo contrario devuelve False.
print(5 <= 3)  # Less than or equal to. Devuelve True si el primer numero es menor o igual que el segundo, de lo contrario devuelve False.

#ordenacion alfabetica
print("hola" == "python")
print("hola" > "python")
print("hola" < "python")

# Operadores logicos
print(3 > 4 and "hola" > "python")
print(3 > 4 or "hola" > "python")
print(3 > 4 or not "hola" > "python")
print(3 > 4 or ("hola" > "python" and 5 > 2))
